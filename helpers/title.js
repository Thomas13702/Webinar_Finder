export const name = (title) => {
  if (title === "computerscience") {
    return "Computer Science";
  } else if (title === "business") {
    return "Business";
  } else if (title === "leadership") {
    return "Leadership";
  } else if (title === "healthandlifestyle") {
    return "Health and lifestyle";
  } else if (title === "food") {
    return "Food";
  } else if (title === "artsandcrafts") {
    return "Arts and Crafts";
  } else if (title === "technology") {
    return "Technology";
  } else if (title === "sport") {
    return "Sport";
  } else if (title === "other") {
    return "Other";
  } else {
    return title;
  }
};
